// Parse TSV
function parseTSV(tsv) {
    const lines = tsv.trim().split(/\r?\n/);
    const headers = lines[0].split('\t');
    return lines.slice(1).map(line => {
        const values = line.split('\t');
        const obj = {};
        headers.forEach((header, index) => {
            let val = values[index];
            if (!isNaN(val) && val !== "") val = Number(val);
            obj[header] = val;
        });
        return obj;
    });
}

const db = {
    primaryBlood: parseTSV(rawPrimaryBlood),
    secondaryBlood: parseTSV(rawSecondaryBlood),
    roots: parseTSV(rawRoots),
    styles: parseTSV(rawStyles)
};

// Lookup helpers
db.getPrimaryBlood = (name) => db.primaryBlood.find(b => b['ブラッド名'] === name);
db.getSecondaryBlood = (name) => db.secondaryBlood.find(b => b['ブラッド名'] === name);
db.getRoot = (name) => db.roots.find(r => r['ルーツ名'] === name);
db.getStyle = (name) => db.styles.find(s => s['スタイル名'] === name);

window.BBTData = db;
